#Получение значений(объектов) словаря с помощью метода values()
print("#Получение значений(объектов) словаря с помощью метода values()")
D = {'spam':2,'ham':1,'eggs':3}
print(D)
print(list(D.values()))
print("#"*40)
#Получение кортежа(ключ, значение) с помощью метода items()
print("#Получение кортежа(ключ, значение) с помощью метода items()")
print(D)
print(list(D.items()))
print("#"*40)
#Использования метода get() для установки значения по умалчанию, для несуществующего элемента словаря
print("Использования метода get() для установки значения по умалчанию, для несуществующего элемента словаря")
print(D.get('spam'))
print(D.get('name'))
print(D.get('name',88))
print("#"*40)
#Используя метод update() можно обьеденить 2 словаря
print("#Используя метод update() можно обьеденить 2 словаря")
D2 = {'name' : 'Artiom', 'say' : 'hello'}
print(D2)
D.update(D2)
print(D)
print("#"*40)
#Удаление ключа=значения с помощью функции pop()
print("#Удаление ключа=значения с помощью функции pop()")
print(D)
D.pop('name')
print(D)
D.pop('say')
print(D)
print("#"*40)

